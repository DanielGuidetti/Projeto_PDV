ON CONFLICT (id) DO NOTHING;


-- ========================================================
-- Políticas de Segurança (RLS Global para Auth)
-- ========================================================

ALTER TABLE public.produtos ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.vendas ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.movimentacoes ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.store_configs ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.scale_configs ENABLE ROW LEVEL SECURITY;

-- Remover políticas restritivas anteriores
DROP POLICY IF EXISTS "Permitir leitura para auth" ON public.vendas;
DROP POLICY IF EXISTS "Permitir inserção para auth" ON public.vendas;
DROP POLICY IF EXISTS "Permitir atualização para auth" ON public.vendas;
DROP POLICY IF EXISTS "Permitir delete para auth" ON public.vendas;
DROP POLICY IF EXISTS "Permitir leitura para auth" ON public.produtos;
DROP POLICY IF EXISTS "Permitir inserção/edição para auth" ON public.produtos;
DROP POLICY IF EXISTS "Permitir leitura para auth" ON public.movimentacoes;
DROP POLICY IF EXISTS "Permitir leitura da propria config" ON public.scale_configs;
DROP POLICY IF EXISTS "Permitir inserção/edição propria config" ON public.scale_configs;

-- Criar novas políticas GLOBAIS (Acesso para qualquer usuário autenticado)

-- PRODUTOS
CREATE POLICY "Acesso Global Produtos" ON public.produtos FOR ALL USING (auth.role() = 'authenticated');

-- VENDAS
CREATE POLICY "Acesso Global Vendas" ON public.vendas FOR ALL USING (auth.role() = 'authenticated');

-- MOVIMENTACOES
CREATE POLICY "Acesso Global Movimentacoes" ON public.movimentacoes FOR ALL USING (auth.role() = 'authenticated');

-- CONFIG LOJA
CREATE POLICY "Acesso Global Store Config" ON public.store_configs FOR ALL USING (auth.role() = 'authenticated');

-- CONFIG BALANÇA
CREATE POLICY "Acesso Global Scale Config" ON public.scale_configs FOR ALL USING (auth.role() = 'authenticated');
