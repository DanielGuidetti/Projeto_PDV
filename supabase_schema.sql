-- Script de Criação e Configuração das Tabelas no Supabase (Versão SaaS Multi-Tenant)
-- Cole este script no SQL Editor do Supabase e clique em 'Run'

-- 1. Tabela de Perfis/Roles (Acesso Administrativo)
CREATE TABLE IF NOT EXISTS public.user_roles (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
    role TEXT NOT NULL DEFAULT 'user' CHECK (role IN ('admin', 'user')),
    UNIQUE(user_id)
);

ALTER TABLE public.user_roles ENABLE ROW LEVEL SECURITY;
DROP POLICY IF EXISTS "Permitir leitura para o próprio usuário" ON public.user_roles;
CREATE POLICY "Permitir leitura para o próprio usuário" ON public.user_roles FOR SELECT USING (user_id = auth.uid());

-- 2. Criação da tabela de Produtos (Tenant)
CREATE TABLE IF NOT EXISTS public.produtos (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    "PLU" TEXT NOT NULL,
    nome TEXT NOT NULL,
    custo NUMERIC(10, 2) DEFAULT NULL,
    preco NUMERIC(10, 2) NOT NULL DEFAULT 0.00,
    estoque NUMERIC(10, 3) NOT NULL DEFAULT 0,
    pesavel BOOLEAN NOT NULL DEFAULT false,
    controlar_estoque BOOLEAN NOT NULL DEFAULT true,
    permitir_estoque_negativo BOOLEAN NOT NULL DEFAULT false,
    codigo_referencia TEXT DEFAULT NULL,
    user_id UUID NOT NULL DEFAULT auth.uid(),
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    UNIQUE("PLU", user_id)
);

-- Garantir colunas novas na tabela produtos (caso já exista no banco)
DO $$ 
BEGIN 
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name='produtos' AND column_name='user_id') THEN
        ALTER TABLE public.produtos ADD COLUMN user_id UUID NOT NULL DEFAULT auth.uid();
    ELSE
        ALTER TABLE public.produtos ALTER COLUMN user_id SET DEFAULT auth.uid();
        -- NOTA: O SET NOT NULL pode falhar se houverem registros sem user_id. Para produção, precisaria dar um UPDATE produtos SET user_id = 'xxx' antes,
        -- mas como estamos adaptando, vamos tentar:
    END IF;
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name='produtos' AND column_name='custo') THEN
        ALTER TABLE public.produtos ADD COLUMN custo NUMERIC(10, 2) DEFAULT NULL;
    END IF;
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name='produtos' AND column_name='permitir_estoque_negativo') THEN
        ALTER TABLE public.produtos ADD COLUMN permitir_estoque_negativo BOOLEAN NOT NULL DEFAULT false;
    END IF;
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name='produtos' AND column_name='codigo_referencia') THEN
        ALTER TABLE public.produtos ADD COLUMN codigo_referencia TEXT DEFAULT NULL;
    END IF;
END $$;

-- Remover a UNIQUE constraint anterior global do PLU e colocar por tenant, se aplicável
ALTER TABLE public.produtos DROP CONSTRAINT IF EXISTS produtos_PLU_key;
DO $$ BEGIN
    IF NOT EXISTS (
        SELECT 1 FROM pg_constraint WHERE conname = 'produtos_plu_user_id_key'
    ) THEN
        ALTER TABLE public.produtos ADD CONSTRAINT produtos_plu_user_id_key UNIQUE("PLU", user_id);
    END IF;
END $$;

-- 3. Criação da tabela de Vendas (Tenant)
CREATE TABLE IF NOT EXISTS public.vendas (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    data TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    total NUMERIC(10, 2) NOT NULL DEFAULT 0.00,
    "totalItens" INTEGER NOT NULL DEFAULT 0,
    cliente TEXT,
    forma_pagamento TEXT NOT NULL,
    itens JSONB NOT NULL,
    status TEXT NOT NULL DEFAULT 'CONCLUIDA' CHECK (status IN ('CONCLUIDA', 'ENCOMENDA', 'CANCELADA')),
    data_conclusao TIMESTAMP WITH TIME ZONE,
    status_entrega TEXT DEFAULT 'PENDENTE',
    valor_pago NUMERIC(10, 2) DEFAULT 0,
    troco NUMERIC(10, 2),
    user_id UUID NOT NULL DEFAULT auth.uid(), 
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- Garantir colunas novas na tabela vendas (caso já exista no banco)
DO $$ 
BEGIN 
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name='vendas' AND column_name='status') THEN
        ALTER TABLE public.vendas ADD COLUMN status TEXT NOT NULL DEFAULT 'CONCLUIDA';
    END IF;
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name='vendas' AND column_name='data_conclusao') THEN
        ALTER TABLE public.vendas ADD COLUMN data_conclusao TIMESTAMP WITH TIME ZONE;
    END IF;
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name='vendas' AND column_name='status_entrega') THEN
        ALTER TABLE public.vendas ADD COLUMN status_entrega TEXT DEFAULT 'PENDENTE';
    END IF;
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name='vendas' AND column_name='valor_pago') THEN
        ALTER TABLE public.vendas ADD COLUMN valor_pago NUMERIC(10, 2) DEFAULT 0;
    END IF;
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name='vendas' AND column_name='troco') THEN
        ALTER TABLE public.vendas ADD COLUMN troco NUMERIC(10, 2);
    END IF;
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name='vendas' AND column_name='user_id') THEN
        ALTER TABLE public.vendas ADD COLUMN user_id UUID NOT NULL DEFAULT auth.uid();
    ELSE
        ALTER TABLE public.vendas ALTER COLUMN user_id SET DEFAULT auth.uid();
    END IF;
END $$;

-- Atualizar restrição de status da venda para incluir CANCELADA e ENCOMENDA
ALTER TABLE public.vendas DROP CONSTRAINT IF EXISTS vendas_status_check;
ALTER TABLE public.vendas ADD CONSTRAINT vendas_status_check CHECK (status IN ('CONCLUIDA', 'ENCOMENDA', 'CANCELADA'));

-- 4. Criação da tabela de Movimentações (Tenant)
CREATE TABLE IF NOT EXISTS public.movimentacoes (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    data_movimentacao TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    produto_id BIGINT NOT NULL REFERENCES public.produtos(id) ON DELETE CASCADE,
    tipo TEXT NOT NULL CHECK (tipo IN ('ENTRADA', 'SAÍDA', 'ESTORNO')),
    quantidade NUMERIC(10, 3) NOT NULL CHECK (quantidade > 0),
    motivo TEXT,
    user_id UUID NOT NULL DEFAULT auth.uid()
);

-- Atualizar restrição de tipo de movimentação para incluir ESTORNO
ALTER TABLE public.movimentacoes DROP CONSTRAINT IF EXISTS movimentacoes_tipo_check;
ALTER TABLE public.movimentacoes ADD CONSTRAINT movimentacoes_tipo_check CHECK (tipo IN ('ENTRADA', 'SAÍDA', 'ESTORNO'));

-- Garantir coluna user_id na tabela movimentacoes
DO $$ 
BEGIN 
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name='movimentacoes' AND column_name='user_id') THEN
        ALTER TABLE public.movimentacoes ADD COLUMN user_id UUID NOT NULL DEFAULT auth.uid();
    ELSE
        ALTER TABLE public.movimentacoes ALTER COLUMN user_id SET DEFAULT auth.uid();
    END IF;
END $$;

-- 5. TABELA DE CONFIGURAÇÃO DA LOJA (Por Usuário)
CREATE TABLE IF NOT EXISTS public.store_configs (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id UUID NOT NULL UNIQUE DEFAULT auth.uid(),
    nome_loja TEXT NOT NULL DEFAULT 'Mercearia',
    endereco TEXT,
    rodape_recibo TEXT,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- Tentar remover restrição antiga se existir
ALTER TABLE public.store_configs DROP CONSTRAINT IF EXISTS store_configs_id_check;

-- 6. Configuração da Balança (Por Usuário)
CREATE TABLE IF NOT EXISTS public.scale_configs (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id UUID NOT NULL UNIQUE DEFAULT auth.uid(),
    prefix_length INTEGER NOT NULL DEFAULT 1,
    plu_length INTEGER NOT NULL DEFAULT 4,
    value_length INTEGER NOT NULL DEFAULT 5,
    value_type TEXT NOT NULL DEFAULT 'price' CHECK (value_type IN ('price', 'weight')),
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- Tentar remover restrição antiga se existir
ALTER TABLE public.scale_configs DROP CONSTRAINT IF EXISTS scale_configs_id_check;

-- Garantir user_id em store_configs
DO $$ 
BEGIN 
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name='store_configs' AND column_name='user_id') THEN
        ALTER TABLE public.store_configs ADD COLUMN user_id UUID DEFAULT auth.uid();
        -- Ignoramos a constraint UNIQUE neste momento caso existam múltiplos registros antigos
    ELSE
        ALTER TABLE public.store_configs ALTER COLUMN user_id SET DEFAULT auth.uid();
    END IF;
END $$;

-- Garantir user_id em scale_configs
DO $$ 
BEGIN 
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name='scale_configs' AND column_name='user_id') THEN
        ALTER TABLE public.scale_configs ADD COLUMN user_id UUID DEFAULT auth.uid();
    ELSE
        ALTER TABLE public.scale_configs ALTER COLUMN user_id SET DEFAULT auth.uid();
    END IF;
END $$;

-- ========================================================
-- Políticas de Segurança Multi-Tenant (Acesso Restrito)
-- ========================================================

ALTER TABLE public.produtos ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.vendas ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.movimentacoes ENABLE ROW LEVEL SECURITY;
